# Explaination: If we want our code to do a specific thing again and again, we are going to use forloops for that.
# For example: there is a string data = "etc" and if we use for loop here, it will start from e like print e first, then t, then c.
# Like loop will start again and again till the end.


data = ("This is data!")

for letters in data:           # This is going to print T then h then i and so on, because forloop will start with first char till end
    print(letters)