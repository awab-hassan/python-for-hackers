# Explaination: Functions are initiated to do a task, for example when you setup a function. You can basically use that function.
# Again and Again instead of coding again the same thing. Lets say there is a add_two function which adds two number to whatever number you input.
# You can simply just use your function again and again instead of repeating a syntax to add two numbers.


def add_two(number):
    result = number + 2
    print(result)

add_two(100)


def multiple_two(number):
    result = number * 2
    print(result)

multiple_two(100)

